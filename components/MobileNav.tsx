'use client';

import { useState } from 'react';
import Link from 'next/link';

interface MobileNavProps {
  activeTab?: 'chat' | 'gmail' | 'files' | 'calendar' | 'more';
  onTabChange?: (tab: string) => void;
}

export function MobileNav({ activeTab = 'chat', onTabChange }: MobileNavProps) {
  const tabs = [
    { id: 'chat', label: 'Chat', icon: ChatIcon },
    { id: 'gmail', label: 'Gmail', icon: MailIcon },
    { id: 'files', label: 'Files', icon: FileIcon },
    { id: 'calendar', label: 'Calendar', icon: CalendarIcon },
    { id: 'more', label: 'More', icon: MoreIcon },
  ];

  return (
    <nav className="mobile-nav fixed bottom-0 left-0 right-0 bg-gray-900 border-t border-gray-800 z-40 safe-padding-bottom">
      <div className="flex items-center justify-around h-16">
        {tabs.map((tab) => {
          const Icon = tab.icon;
          const isActive = activeTab === tab.id;

          return (
            <button
              key={tab.id}
              onClick={() => onTabChange?.(tab.id)}
              className={`flex flex-col items-center justify-center flex-1 h-full transition-colors ${
                isActive ? 'text-blue-500' : 'text-gray-400 hover:text-white'
              }`}
              aria-label={tab.label}
            >
              <Icon className="w-6 h-6 mb-1" />
              <span className="text-xs font-medium">{tab.label}</span>
            </button>
          );
        })}
      </div>
    </nav>
  );
}

// Icon components
